# while 문 맨 처음으로 돌아가기 - continue
a = 0                  # a는 0 부터 시작함
while a < 10:          # 0 부터 시작이니 이 조건은 참 (아래 반복문 실행행)
    a += 1             # a + 1을 시켜줌
    if a % 2 == 0:     # 그 숫자가 나머지 -0이 나온다면(== 짝수라면)
        continue       # 맨 처음으로 돌아가 다시 실행
    print(a)           # 짝수가 아니면 print
