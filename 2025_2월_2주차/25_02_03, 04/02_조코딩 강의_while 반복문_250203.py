# 나무 찍기 예제
treeHit = 0                                    # 나무 0번 찍은 상태
while treeHit < 10:                            # 나무 찍기를 10번 초과 될 때 까지 반복
    treeHit = treeHit + 1                      # 나무 찍기를 반복하면서 횟수 + 1 씩 추가
    print("나무를 %d번 찍었습니다" % treeHit)    # 나무를 몇 번 찍었는지 포매팅으로 확인
    if treeHit == 10:                          # if 조건문으로 나무 찍기 10회 달성 시
        print("나무 넘어갑니다.")               # "나무 넘어갑니다." 출력
                                               # 10회 초과되면서 while 반복문도 종료됨.


